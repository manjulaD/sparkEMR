package com.example.spark;

public class ABC {

    private String a_id;
    private String w_id;
    private String p_id;
    private String t_id;
    private String n_id;
    private String as_id;
    private String r_id;
    private String m_id;

    public ABC(String a_id, String w_id, String p_id, String t_id, String n_id, String as_id){
        this.a_id = a_id;
        this.w_id = w_id;
        this.p_id = p_id;
        this.t_id = t_id;
        this.n_id = n_id;
        this.as_id = as_id;
    }

    public ABC(){}

    public String getA_id() {
        return a_id;
    }

    public void setA_id(String a_id) {
        this.a_id = a_id;
    }

    public String getW_id() {
        return w_id;
    }

    public void setW_id(String w_id) {
        this.w_id = w_id;
    }

    public String getP_id() {
        return p_id;
    }

    public void setP_id(String p_id) {
        this.p_id = p_id;
    }

    public String getT_id() {
        return t_id;
    }

    public void setT_id(String t_id) {
        this.t_id = t_id;
    }

    public String getN_id() {
        return n_id;
    }

    public void setN_id(String n_id) {
        this.n_id = n_id;
    }

    public String getAs_id() {
        return as_id;
    }

    public void setAs_id(String as_id) {
        this.as_id = as_id;
    }

    public String getR_id() {
        return r_id;
    }

    public void setR_id(String r_id) {
        this.r_id = r_id;
    }

    public String getM_id() {
        return m_id;
    }

    public void setM_id(String m_id) {
        this.m_id = m_id;
    }
}

